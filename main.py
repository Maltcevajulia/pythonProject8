        y2 = 1.1 # координата У шара, изначально покоящегося
        V0 = 2 # начальная скорость шара, по которому бьют
        V2 = 0 # начальная скорость покоящегося шара

        functions.collusions_calculator(V0, V2, x1, y1, dt, n, g, x2, y2, x0, y0, E, i, k, m, r, Lx, Ly, R) # функция, осуществляющая вычисление столкновений шаров и подчёт их энергий
